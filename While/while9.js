function findDivisors(num) {
  let i = 1; // Начинаем проверку с 1
  console.log(`Делители числа ${num}:`);

  // Цикл продолжается, пока i меньше или равно самому числу
  while (i <= num) {
    // Проверяем, делится ли num на i без остатка
    if (num % i === 0) {
      console.log(i); // Если делится, выводим i
    }
    i++; // Увеличиваем i для перехода к следующему числу
  }
}

findDivisors(12);