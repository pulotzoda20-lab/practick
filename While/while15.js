let count = 0; // Счетчик положительных чисел
let number;

while (true) {
    // Запрашиваем ввод пользователя
    number = prompt('Введите число (или отрицательное число для завершения):');

    // Преобразуем ввод в число
    const parsedNumber = parseInt(number);

    // Проверяем, является ли введенное значение корректным числом
    if (isNaN(parsedNumber)) {
        alert('Вы ввели не число. Пожалуйста, попробуйте снова.');
        continue; // Пропускаем текущую итерацию и просим ввести снова
    }

    // Если пользователь ввел отрицательное число, выходим из цикла
    if (parsedNumber < 0) {
        break;
    }

    // Если число положительное, увеличиваем счетчик
    count++;
}

console.log(`Количество положительных чисел: ${count}`);