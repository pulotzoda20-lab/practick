let count = 0; // Инициализация счетчика
let userInput = ""; // Переменная для хранения ввода пользователя

while (userInput !== "stop") {
  // Запрашиваем ввод пользователя
  userInput = prompt("Введите строку (для остановки введите 'stop'):");

  // Проверяем, не ввел ли пользователь "stop"
  if (userInput !== "stop") {
    count++; // Увеличиваем счетчик, если строка не равна "stop"
    console.log(`Введена строка: "${userInput}"`); // Опционально: выводим введенную строку
  }
}

console.log(`Общее количество введенных строк (до 'stop'): ${count}`);