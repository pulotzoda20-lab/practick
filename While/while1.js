let sum = 0; // Инициализируем переменную для суммы

while (true) { // Бесконечный цикл
    let input = prompt(0); // Получаем ввод от пользователя
    let number = parseInt(input); // Преобразуем ввод в число

    if (number === 0) { // Проверяем, если введено 0
        break; // Выходим из цикла
    }

    sum += number; // Добавляем число к общей сумме
}

console.log(`Сумма введенных чисел: ${sum}`); // Выводим результат