function fibonacciUntilM(M) {
  let a = 0; // Первое число Фибоначчи
  let b = 1; // Второе число Фибоначчи
  let nextFib; // Следующее число Фибоначчи

  // Выводим первое число (0), если оно не превышает M
  if (a <= M) {
    console.log(a);
  }

  // Начинаем цикл, пока текущее число (b) не превышает M
  while (b <= M) {
    console.log(b); // Выводим текущее число
    nextFib = a + b; // Вычисляем следующее число
    a = b; // Сдвигаем значения: предыдущее становится текущим
    b = nextFib; // Сдвигаем значения: текущее становится следующим
  }
}

fibonacciUntilM(50);