let key = 8;

switch (key) {
    case 1:
        console.log('Mercury');
        break;
    case 2:
        console.log('Venus');
        break;
    case 3:
        console.log('Earth');
        break;
    case 4:
        console.log('Mars');
        break;
    case 5:
        console.log('Jupiter');
        break;
    case 6:
        console.log('Saturn');
        break;
    case 7:
        console.log('Uranus');
        break;
    case 8:
        console.log('Neptune');
        break;
    default:
        break;
}