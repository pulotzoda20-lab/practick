let keyN = 'N';
keyS = 'S';
keyW = 'W';
keyE = 'E';

switch (keyN, keyS, keyW, keyE) {
    case 'N':
        console.log('North');
        break;
    case 'S':
        console.log('South');
        break;
    case 'W':
        console.log('West');
        break;
    case 'E':
        console.log('East');
        break;
    default:
        break;
}